public class SwitchTest {
  public static void main(String[] args) {
    int i = 4;
    switch (i) {
    case 1 :
      System.out.println("one");
      break;
    case 2 :
      System.out.println("two");
      break;
    case 3 :
      System.out.println("three");
      break;
    default:
      System.out.println("other");
    }

  }
}
